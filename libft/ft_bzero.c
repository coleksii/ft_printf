#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	int		i;
	char	*str1;

	str1 = (char *)str;
	i = 0;
	while (n > 0)
	{
		str1[i] = '\0';
		i++;
		n--;
	}
}
