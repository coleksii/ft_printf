#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[b] != '\0' && n > 0)
	{
		s1[i++] = s2[b++];
		--n;
	}
	s1[i] = '\0';
	return (s1);
}
